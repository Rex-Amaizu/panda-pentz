// route to manage an already created pentz gallery.
router.get('/manage', async (req, res) => {

    const pentZName = req.query.gallery_name;

    const nft = await Nft.findOne({gallery_name: pentZName});
  
    if (!nft) return res.status(404).send('This Pentz gallery does not exist. Choose Create to create new gallery with this Pentz.');
  
    res.send(nft);
});

// route to add an nft to an already created pentz gallery.
router.post('/add', async (req, res) => {

    const new_nft = req.body.nft; // getting object trying to add
    const filter = req.body.gallery; // getting filter to target the collection

    const nft = await Nft.findOneAndUpdate(filter, { $addToSet:  {nfts: new_nft}}, {new:true});

    if (!nft) return res.status(404).send('This Pentz Gallery does not exist. Try Again');
    
    res.json({success: "New nft added to gallery successfully", nft});
});

// route to delete an nft from from a pentz gallery.
router.post('/del', async (req, res) => {

    const del_nft = req.body.nft; // getting object to delete
    const filter = req.body.gallery; // getting filter to target the collection

    const nft = await Nft.findOneAndUpdate(filter, { $pull:  {nfts: del_nft}}, {new:true});

    if (!nft) return res.status(404).send('This Pentz Gallery does not exist. Try Again');
    
    res.json({success: "Nft deleted successfully"});
});

// route to save a pentz gallery with the associated nfts.
router.post('/save', async (req, res) => {

    // defining the request parameters.
    const gName = req.body.gallery_name;
    const gImage = req.body.gallery_image;
    const gNfts = req.body.nfts;

    // checking if gallery exist.
    let pentZName = await Nft.findOne({ gallery_name: gName });
    if (pentZName) return res.status(400).send('This Pentz Gallery already Exist! Choose Another Pentz to create new gallery OR click Manage Gallery button to manage existing gallery');
    
    // ensuring a user adds at least one nft to a pentz gallery.
    if((gNfts === undefined) || (gNfts.length == 0)) return res.send("A Pentz Gallery must contain at least 1 Nft");
    
    // ensuring a user does not add more than 5 nfts to a pentz gallery.
    if(((gNfts.length) > 5)) return res.send("You cannot add more than 5 Nfts to one Pentz Gallery");

    // initiating and saving the nft collection.
    const nft = await new Nft({ 
      gallery_name: gName,
      gallery_image: gImage,
      nfts: gNfts
    });
    await nft.save();
    
    // returning the response with the created gallery.
    res.json({success : "New Pentz Gallery Created Successfully", 
    gallery_name: nft.gallery_name, 
    gallery_image: nft.gallery_image, 
    nfts: nft.nfts
    });
});

